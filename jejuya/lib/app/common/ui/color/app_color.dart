import 'package:flutter/material.dart';

/// An abstract class that defines the color scheme of the app.
abstract class AppColor {
  /// The primary background color.
  Color get primaryBackground;

  /// The primary color.
  Color get primaryColor;

  /// The black color.
  Color get black;

  /// The info color.
  Color get info;

  /// The white color.
  Color get white;

  /// The white2 color.
  Color get white2;

  /// The containerBackground color.
  Color get containerBackground;

  /// The red color.
  Color get red;

  /// The darkRed color.
  Color get darkRed;

  /// The primaryLight color.
  Color get primaryLight;
}
